/**
 * 游戏数据结构
 */
var mongoose = require('mongoose');
var Schema = mongoose.Schema;
var CostActiveSchema = new Schema({
	createBy:{
		type:Schema.ObjectId,
		ref:'User',
		require:true
	},
	channel:{
		type:Schema.ObjectId,
		ref:'User',
		require:true
	},
	channelName:{
		type:String,
		require:true
	},
	inputDate:{
		type:Number,
		require:true
	},
	gameName:{
		type:String,
		require:true
	},
	installAmount:{
		type:Number,
		require:true,
		default:0
	},
	singlePrize:{
		type:Number,
		require:true,
		default:0
	},
	settlementAmount:{
		type:Number,
		require:true,
		default:0
	},
	pinYin:[
		{
			type:String,
			require:false
		}
	],
	createTime:{
		type:Number,
		require:true
	},
	createIp:{
		type:String,
		require:true
	},
	lastEditTime:{
		type:Number,
		require:false
	}
});
var CostSalesSchema = new Schema({
	createBy:{
		type:Schema.ObjectId,
		ref:'User',
		require:true
	},
	channel:{
		type:Schema.ObjectId,
		ref:'User',
		require:true
	},
	channelName:{
		type:String,
		require:true
	},
	inputDate:{
		type:Number,
		require:true
	},
	gameName:{
		type:String,
		require:true
	},
	additionUser:{
		type:Number,
		require:true
	},
	totalStream:{
		type:Number,
		require:true,
		default:0
	},
	arpu:{
		type:Number,
		require:true
	},
	splitRatio:{
		type:Number,
		require:true
	},
	settlementAmount:{
		type:Number,
		require:true,
		default:0
	},
	pinYin:[
		{
			type:String,
			require:false
		}
	],
	createTime:{
		type:Number,
		require:true
	},
	createIp:{
		type:String,
		require:true
	},
	lastEditTime:{
		type:Number,
		require:false
	}
});
exports.CostActiveSchema = CostActiveSchema;
exports.CostSalesSchema = CostSalesSchema;